class AnalyzeRequest(BaseModel):
    """Request model for the legal risk detection endpoint."""
    document: str = Field(..., description="A URL pointing to the sponsorship agreement document (PDF, DOCX, etc.).")
    format: Literal["json", "text"] = Field("text", description="Output format: 'text' for human-readable report, 'json' for structured JSON.")
