@app.post('/hackrx/run', response_model=JobResponse)
async def process_job(request: JobRequest):
    """
    Processes a document, answers questions about it, and returns the results.
    This endpoint handles document indexing (if new) and the full RAG pipeline.
    """
    document_url = request.documents
    document_id = generate_document_id(document_url)

    # --- STAGE 1: DOCUMENT PROCESSING & INDEXING ---
    await _ingest_document_if_needed(document_url, document_id)

    # --- STAGE 2: QUESTION ANSWERING ---
    try:
        print(f"🤖 Starting Q&A pipeline for {len(request.questions)} questions...")
        # [cite_start]Call the main question-answering function from the worker module [cite: 2]
        answers = await answer_queries(
            queries=[(q, document_id) for q in request.questions],
            services=services,
            semantic_weight=request.semantic_weight,
            keyword_weight=request.keyword_weight,
            k_semantic=request.k_semantic,
            k_keyword=request.k_keyword,
            k_rerank=request.k_rerank
        )
        print("✅ Q&A pipeline complete.")
        # [cite_start]Return the job ID and the generated answers [cite: 2]
        return {"job_id": document_id, "answers": answers}
    except Exception as e:
        traceback.print_exc()
        raise HTTPException(status_code=500, detail=f"Failed to generate answers: {str(e)}")

@app.post('/analyze')
async def analyze_document(request: AnalyzeRequest):
    """
    Analyzes a sponsorship agreement for risky, ambiguous, or potentially harmful clauses.
    Ingests the document (if not already indexed), then runs legal risk detection using
    hybrid retrieval + reranking with specialized legal risk queries.
    """
    document_url = request.document
    document_id = generate_document_id(document_url)

    # --- STAGE 1: DOCUMENT PROCESSING & INDEXING ---
    await _ingest_document_if_needed(document_url, document_id)

    # --- STAGE 2: LEGAL RISK ANALYSIS ---
    try:
        print(f"⚖️ Starting legal risk detection pipeline...")
        risky_clauses = await analyze_legal_risks(
            document_id=document_id,
            services=services,
            semantic_weight=request.semantic_weight,
            keyword_weight=request.keyword_weight,
            k_semantic=request.k_semantic,
            k_keyword=request.k_keyword,
            k_rerank=request.k_rerank
        )
        print(f"✅ Legal risk analysis complete. Found {len(risky_clauses)} risky clauses.")

        high = sum(1 for c in risky_clauses if c.get("risk_level") == "high")
        medium = sum(1 for c in risky_clauses if c.get("risk_level") == "medium")
        low = sum(1 for c in risky_clauses if c.get("risk_level") == "low")

        response_data = AnalyzeResponse(
            document_id=document_id,
            total_risks_found=len(risky_clauses),
            high_risk_count=high,
            medium_risk_count=medium,
            low_risk_count=low,
            risky_clauses=[RiskyClause(**c) for c in risky_clauses]
        )

        if request.format == "text":
            return PlainTextResponse(content=format_risk_report(response_data))
        return response_data
    except Exception as e:
        traceback.print_exc()
        raise HTTPException(status_code=500, detail=f"Failed to analyze document: {str(e)}")


